package prac;

public class Wrapperclas {

	public static void main(String[] args) {
		
		// converting integer to int 
		
		Integer a = new Integer(3);
		
		int i = a.intValue(); // converting integer to int
		
		int j = a; // unboxing 
		
		
		System.out.println(a+ " "+ i +" "+ j);
		
		
				
	}

}
